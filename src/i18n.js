export function detectLanguage() {
  return navigator.language && navigator.language.toLowerCase().startsWith("es") ? "es" : "en";
}

export function t(key, vars = {}, language = getActiveLanguage()) {
  const dictionary = dictionaries[language] || dictionaries.en;
  const template = dictionary[key] || dictionaries.en[key] || key;
  return Object.entries(vars).reduce(
    (value, [varKey, varValue]) => value.replaceAll(`{${varKey}}`, String(varValue)),
    template
  );
}

let activeLanguage = detectLanguage();

export function setActiveLanguage(language) {
  activeLanguage = dictionaries[language] ? language : "en";
  document.documentElement.lang = activeLanguage;
}

export function getActiveLanguage() {
  return activeLanguage;
}

export function getDictionaries() {
  return dictionaries;
}
